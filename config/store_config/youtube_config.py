# 过滤后保存的视频条数（默认：None - 保存所有通过过滤的视频）
# 如果设置为数字，则只保存前 N 个通过过滤的视频
# 例如：MAX_SAVE_COUNT = 50 表示只保存前50个视频
MAX_SAVE_COUNT = 3

# 视频保存优先级配置
# 优先级列表，按优先级从高到低排序
# 可选值：'resolution'（分辨率）、'view_count'（播放次数）、'publish_time'（发布时间）
# 默认：[] - 按照搜索顺序保存（不排序）
# 示例：['resolution', 'view_count', 'publish_time'] 表示优先按分辨率排序，然后按播放次数，最后按发布时间
PRIORITY_ORDER = ['view_count', 'publish_time', 'resolution']

# 优先级排序方向配置
# 'desc' 表示降序（高到低），'asc' 表示升序（低到高）
# 例如：分辨率降序表示 4K > 1080p > 720p
PRIORITY_SORT_DIRECTION = {
    'resolution': 'desc',      # 分辨率：降序（4K > 1080p > 720p）
    'view_count': 'desc',      # 播放次数：降序（播放量高的在前）
    'publish_time': 'asc'      # 发布时间：升序（最新的在前，即天数少的在前）
}

